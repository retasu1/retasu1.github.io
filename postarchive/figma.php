				<p>The goal was to gain a good grasp of Figma, while developing my UI design senses along the way. Understanding the basics of Figma was not too difficult. As the days progressed, I tried to incorporate more Figma features: styles, components/assets, plugins. At the current time of writing (finished Day 066), I am quite comfortable working with Figma, and am experimenting with new concepts in UI design (e.g. various grid layouts).</p>
